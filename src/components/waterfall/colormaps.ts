export type ColormapName = 'gqrx' | 'rainbow' | 'viridis' | 'twentev2';

export function createPalette(name: ColormapName): Uint32Array {
  const lut = new Uint32Array(256);
  for (let i = 0; i < 256; i++) {
    const t = i / 255;
    const rgb =
      name === 'gqrx'
        ? gqrxColor(i)
        : name === 'rainbow'
          ? rainbowColor(t)
          : name === 'viridis'
            ? viridisColor(t)
            : twenteV2Color(i);
    lut[i] = (rgb.r << 24) | (rgb.g << 16) | (rgb.b << 8) | 0xff;
  }
  return lut;
}

function gqrxColor(i: number): { r: number; g: number; b: number } {
  if (i < 20) return { r: 0, g: 0, b: 0 };
  if (i < 70) {
    const t = (i - 20) / 50;
    return { r: 0, g: 0, b: Math.round(140 * t) };
  }
  if (i < 100) {
    const t = (i - 70) / 30;
    return { r: Math.round(60 * t), g: Math.round(125 * t), b: Math.round(115 * t + 140) };
  }
  if (i < 150) {
    const t = (i - 100) / 50;
    return { r: Math.round(195 * t + 60), g: Math.round(130 * t + 125), b: Math.round(255 - 255 * t) };
  }
  if (i < 250) {
    const t = (i - 150) / 100;
    return { r: 255, g: Math.round(255 - 255 * t), b: 0 };
  }
  const t = (i - 250) / 5;
  return { r: 255, g: Math.round(255 * t), b: Math.round(255 * t) };
}

function rainbowColor(t: number): { r: number; g: number; b: number } {
  const clamped = Math.min(1, Math.max(0, t));
  const h = (1 - clamped) * 240;
  return hslToRgb(h, 100, 50);
}

function viridisColor(t: number): { r: number; g: number; b: number } {
  const stops: Array<{ t: number; r: number; g: number; b: number }> = [
    { t: 0.0, r: 68, g: 1, b: 84 },
    { t: 0.13, r: 71, g: 44, b: 122 },
    { t: 0.25, r: 59, g: 81, b: 139 },
    { t: 0.38, r: 44, g: 113, b: 142 },
    { t: 0.5, r: 33, g: 144, b: 141 },
    { t: 0.63, r: 39, g: 173, b: 129 },
    { t: 0.75, r: 92, g: 200, b: 99 },
    { t: 0.88, r: 170, g: 220, b: 50 },
    { t: 1.0, r: 253, g: 231, b: 37 },
  ];

  const clamped = Math.min(1, Math.max(0, t));
  let lo = stops[0];
  let hi = stops[stops.length - 1];
  for (let i = 1; i < stops.length; i++) {
    if (clamped <= stops[i].t) {
      lo = stops[i - 1];
      hi = stops[i];
      break;
    }
  }

  const span = hi.t - lo.t;
  const u = span <= 0 ? 0 : (clamped - lo.t) / span;
  return {
    r: Math.round(lo.r + (hi.r - lo.r) * u),
    g: Math.round(lo.g + (hi.g - lo.g) * u),
    b: Math.round(lo.b + (hi.b - lo.b) * u),
  };
}

function hslToRgb(h: number, s: number, l: number): { r: number; g: number; b: number } {
  const hh = ((h % 360) + 360) % 360;
  const ss = Math.min(1, Math.max(0, s / 100));
  const ll = Math.min(1, Math.max(0, l / 100));

  const c = (1 - Math.abs(2 * ll - 1)) * ss;
  const x = c * (1 - Math.abs(((hh / 60) % 2) - 1));
  const m = ll - c / 2;

  let r1 = 0;
  let g1 = 0;
  let b1 = 0;
  if (hh < 60) {
    r1 = c;
    g1 = x;
  } else if (hh < 120) {
    r1 = x;
    g1 = c;
  } else if (hh < 180) {
    g1 = c;
    b1 = x;
  } else if (hh < 240) {
    g1 = x;
    b1 = c;
  } else if (hh < 300) {
    r1 = x;
    b1 = c;
  } else {
    r1 = c;
    b1 = x;
  }

  return {
    r: Math.round((r1 + m) * 255),
    g: Math.round((g1 + m) * 255),
    b: Math.round((b1 + m) * 255),
  };
}

function twenteV2Color(i: number): { r: number; g: number; b: number } {
  const idx = Math.max(0, Math.min(255, i | 0));
  const row = TWENTE_V2[idx] ?? [0, 0, 0];
  return {
    r: Math.round(row[0] * 255),
    g: Math.round(row[1] * 255),
    b: Math.round(row[2] * 255),
  };
}

